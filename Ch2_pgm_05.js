// Assigning text to variables

var message;

message = "Hello " + "World!"; // Split the first message into two pieces
console.log(message);

message = 'Congratulations! ' + 'Your tweet has won ' + 'a prize...'; // Split the second message into three pieces
console.log(message);

message = "This is the third message."; // Assign message a third value
console.log(message);




